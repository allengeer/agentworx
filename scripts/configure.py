def setup_environment_file():
    """Help user create .env file."""
    print("ğŸ› ï¸  Environment File Setup")
    print("=" * 30)
    
    env_path = project_root / ".env"
    example_path = project_root / ".env.example"
    
    if env_path.exists():
        print(f"ğŸ“„ .env file already exists at: {env_path}")
        overwrite = input("Do you want to overwrite it? (y/N): ").lower().strip()
        if overwrite != 'y':
            print("Keeping existing .env file")
            return
    
    if not example_path.exists():
        print(f"âŒ .env.example not found at: {example_path}")
        return
        
    # Copy example to .env
    with open(example_path, 'r') as src, open(env_path, 'w') as dst:
        dst.write(src.read())
    
    print(f"âœ… Created .env file at: {env_path}")
    print("ğŸ“ Please edit the .env file and add your API keys")
    print("ğŸ”‘ Required: OPENAI_API_KEY")
    print("ğŸ”‘ Optional: LANGSMITH_API_KEY, JIRA_API_TOKEN")


def prompt_for_keys():
    """Interactive prompt for API keys."""
    print("ğŸ” API Key Setup")
    print("=" * 20)
    
    env_path = project_root / ".env"
    env_vars = {}
    
    # Load existing .env if it exists
    if env_path.exists():
        with open(env_path, 'r') as f:
            for line in f:
                line = line.strip()
                if '=' in line and not line.startswith('#'):
                    key, value = line.split('=', 1)
                    env_vars[key] = value
    
    # Prompt for OpenAI key (required)
    current_openai = env_vars.get('OPENAI_API_KEY', '')
    if current_openai and not current_openai.startswith('your_'):
        print(f"ğŸ”‘ OpenAI API Key: Already configured")
    else:
        openai_key = input("ğŸ”‘ Enter your OpenAI API Key (required): ").strip()
        if openai_key:
            env_vars['OPENAI_API_KEY'] = openai_key
    
    # Prompt for LangSmith key (optional)
    current_langsmith = env_vars.get('LANGSMITH_API_KEY', '')
    if current_langsmith and not current_langsmith.startswith('your_'):
        print(f"ğŸ“Š LangSmith API Key: Already configured")
    else:
        print("\nğŸ“Š LangSmith is used for evaluation and tracing (optional)")
        langsmith_key = input("Enter your LangSmith API Key (press Enter to skip): ").strip()
        if langsmith_key:
            env_vars['LANGSMITH_API_KEY'] = langsmith_key
    
    # Write updated .env file
    with open(env_path, 'w') as f:
        f.write("# Sengy Configuration\n")
        f.write("# Generated by configure.py\n\n")
        
        for key, value in env_vars.items():
            f.write(f"{key}={value}\n")
    
    print(f"âœ… Configuration saved to: {env_path}")


def main():
    """Main configuration menu."""
    while True:
        print("\nğŸ› ï¸  Sengy Configuration Manager")
        print("=" * 35)
        print("1. Check current configuration")
        print("2. Create .env file from template")
        print("3. Set up API keys interactively")
        print("4. Reload configuration")
        print("5. Exit")
        
        choice = input("\nSelect an option (1-5): ").strip()
        
        if choice == '1':
            check_configuration()
        elif choice == '2':
            setup_environment_file()
        elif choice == '3':
            prompt_for_keys()
        elif choice == '4':
            print("ğŸ”„ Reloading configuration...")
            reload_settings()
            print("âœ… Configuration reloaded")
        elif choice == '5':
            print("ğŸ‘‹ Goodbye!")
            break
        else:
            print("âŒ Invalid choice, please try again")


if __name__ == "__main__":
    main()